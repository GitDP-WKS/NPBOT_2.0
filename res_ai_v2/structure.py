def normalize_res(value: Any) -> str:
    original = _clean(value)
    if not original:
        return ""
    return RES_ALIASES.get(_res_key(original), original)


def normalize_branch(value: Any) -> str:
    original = _clean(value)
    if not original:
        return ""
    return BRANCH_ALIASES.get(_key(original), original)


def branch_for_res(value: Any) -> str:
    return CURRENT_STRUCTURE.get(normalize_res(value), "")


def canonical_executor(branch: Any, res: Any) -> tuple[str, str, bool]:
    canonical_res = normalize_res(res)
    known = canonical_res in CURRENT_STRUCTURE
    canonical_branch = branch_for_res(canonical_res) if known else normalize_branch(branch)
    return canonical_branch, canonical_res, known
