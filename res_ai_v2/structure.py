_SPACE = re.compile(r"\s+")


def _clean(value: Any) -> str:
    if value is None:
        return ""
    return _SPACE.sub(" ", str(value).replace("\xa0", " ").replace("\u200b", " ")).strip()


def _key(value: Any) -> str:
    text = _clean(value).lower().replace("ё", "е")
    text = re.sub(r"[^а-я0-9]+", " ", text)
    return _SPACE.sub(" ", text).strip()


def _res_key(value: Any) -> str:
    text = _key(value)
    text = re.sub(r"\bгородской район электрических сетей\b", " городской", text)
    text = re.sub(r"\bрайон электрических сетей\b", "", text)
    text = re.sub(r"\bэлектрических сетей\b", "", text)
    text = re.sub(r"\bрэс\b", "", text)
    return _SPACE.sub(" ", text).strip()


def _instrumental(base: str) -> str:
    if base.endswith("ский"):
        return base[:-4] + "ским"
    if base.endswith("цкий"):
        return base[:-4] + "цким"
    if base.endswith("ый"):
        return base[:-2] + "ым"
    if base.endswith("ий"):
        return base[:-2] + "им"
    return base


